	/**
	 * Run the application
	 */
	public void run(){
		//infinite loop
		for(;;){
			startTime = System.currentTimeMillis();
			if(!paused){
				//move the baddie unless he will go into a wall
				if(!baddie.move(baddie.checkDirX() * spd/2, baddie.checkDirY() * spd/2))
					if(!baddie.move(0, baddie.checkDirY() * spd/2))
						baddie.move(baddie.checkDirX() * spd/2, 0);
				//call moveLogic method for the man
				moveLogic();
				//check to see if the coins have been picked up
				coins[levelY][levelX].hitTest();
				//repaint the world
				repaint();
			}
			try{
				endTime = System.currentTimeMillis();
				if(framePeriod - (endTime-startTime)>0)
					Thread.sleep(framePeriod-(endTime-startTime));
			}catch(InterruptedException e){
			}
		}
	}
	
